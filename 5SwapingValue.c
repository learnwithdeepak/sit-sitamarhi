#include<stdio.h>
int main ()
{
    int x,t,y;
    x=5;
    y=10;
    printf("Before swaping the value of X=%d and Value of Y=%d\n",x,y);
    t=x;
    x=y;
    y=t;
    printf("After swaping the value of X=%d and value of Y=%d\n",x,y);
    return 0;
}
